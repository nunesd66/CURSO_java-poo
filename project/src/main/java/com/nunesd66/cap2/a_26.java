package com.nunesd66.cap2;

// 2.26. Operadores de incremento e decremento
public class a_26 {
    public static void main(String[] args) {
        int idade = 10;

        int novaIdade = --idade;

        System.out.println("Idade: " + idade);
        System.out.println("Nova idade: " + novaIdade);
    }
}
